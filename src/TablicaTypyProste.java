public class TablicaTypyProste {
    public static void main(String[] args) {
        // Tworzenie tablicy typów prostych (int)
        int[] tablica = {41, 13, 17, 5, 2};

        // Odwoływanie się do elementów tablicy
        // Wyświetlanie pierwszego elementu tablicy
        System.out.println("Pierwszy element tablicy: " + tablica[0]);
        // Wyświetlanie drugiego elementu tablicy
        System.out.println("Drugi element tablicy: " + tablica[1]);
        // Wyświetlanie trzeciego elementu tablicy
        System.out.println("Trzeci element tablicy: " + tablica[2]);
        // Wyświetlanie czwartego elementu tablicy
        System.out.println("Czwarty element tablicy: " + tablica[3]);
        // Wyświetlanie piątego elementu tablicy
        System.out.println("Piąty element tablicy: " + tablica[4]);
    }
}
